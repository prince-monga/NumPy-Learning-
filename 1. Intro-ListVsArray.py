import numpy as np

# Python list
py_list = [1, 2, 3, 4]
print("Python List:", py_list)
print("List multiplied by 2:", py_list * 2)  # List repetition

# NumPy array
np_array = np.array([1, 2, 3, 4])
print("\nNumPy Array:", np_array)
print("Array multiplied by 2:", np_array * 2)  # Element-wise multiplication

# List addition
print("List + List:", py_list + py_list)  # Concatenation

# Array addition
print("Array + Array:", np_array + np_array)  # Element-wise addition